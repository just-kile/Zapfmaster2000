package de.kile.zapfmaster2000.rest.api.news;

public class NewsRequest {

	private int start;

	private int length;

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getLength() {
		return length;
	}

	public void setLength(int length) {
		this.length = length;
	}

}
